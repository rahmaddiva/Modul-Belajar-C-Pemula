using namespace std;

int main()
{
	int x;
	
	x =7;
	
	cout<<"Nilai x nilai awal : " << x << endl;
	cout << "Nilai ++x : " << ++x << endl;
	cout<< " Nilai x nilai akhir : " << x << endl <<endl;
	
	x = 10;
	
	cout<<"Nilai x nilai awal : "<< x <<endl;
	cout << "Nilai ++x : " << x++ << endl;
	cout<< " Nilai x nilai akhir : " << x << endl <<endl;
	
	return 0;
	
}
