#include <iostream>

using namespace std;

int main()
{
    cout<<"PENGULANGAN INCREMENT ( NAIK SATU NILAI)" << endl;
    for (int A=0; A<10; A++)
    {
       cout << A << endl;
    }

    cout << endl;

    cout<<"PENGULANGAN DECREMENT ( TURUN SATU NILAI)" << endl;
    for (int B=10; B>00; B--)
    {
        cout << B << endl;
    }
    return 0;
}